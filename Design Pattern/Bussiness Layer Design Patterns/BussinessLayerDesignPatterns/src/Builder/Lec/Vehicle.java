package Builder.Lec;

public class Vehicle {

    private String year;
    private String owner;
    private double cost;

    public String getYear() {
        return year;
    }

    public void setYear(String year){
        this.year = year;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner){
        this.owner = owner;
    }

    public double getCost() {
        return cost;
    }

    public void setCost(double cost){
        this.cost = cost;
    }

}
